var builder = WebApplication.CreateBuilder(args);
    builder.Services.Configure<WebSettings>(builder.Configuration.GetSection("WebSettings"));

// Add services to the container.
builder.Services.AddControllersWithViews();

builder.Services.AddAuthentication("MyCookieAuth").AddCookie("MyCookieAuth", options =>
{
    options.Cookie.Name = "MyAuthCookie";
    options.LoginPath = "/Home/Login"; // Specify the login path
    options.AccessDeniedPath = "/Home/AccessDenied"; // Specify the access denied path
});


var app = builder.Build();

// Configure the HTTP request pipeline.
if (!app.Environment.IsDevelopment())
{
    app.UseExceptionHandler("/Home/Error");
    app.UseHsts();
}

app.UseHttpsRedirection();
app.UseStaticFiles();

app.UseRouting();

app.UseAuthentication();
app.UseAuthorization();

app.MapControllerRoute(
    name: "default",
    pattern: "{controller=Home}/{action=Index}/{id?}");

app.Run();

